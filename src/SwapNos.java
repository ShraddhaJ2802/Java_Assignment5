public class SwapNos {
    public static void main(String[] args)
    {

        int x=10, y=5, t;// x and y are to swap

        System.out.println("before swapping numbers: "+x +"  "+ y);
        /*swapping */
        t = x;
        x = y;
        y = t;
        System.out.println("After swapping: "+x +"   " + y);
        System.out.println( );

    }
}
